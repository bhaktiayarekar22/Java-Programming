public class Basic_Array
{
    public static void main(String[] args) {
        int[] arr = new int[5];
        arr[0] = 23;
        arr[1] = 45;
        arr[2] = 34;
        arr[3] = 57;
        arr[4] = 89;
        
        //    0    1    2    3    4    - > index 
        // [ 23 , 45 , 34 , 57 , 89]

        System.out.println(arr[4]);
    }
}